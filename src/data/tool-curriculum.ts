import type { Course, HanziItem, HanziPoemLink, PoemLibraryEntry } from './curriculum';

export type CharacterGroupRule =
  | 'same-radical'
  | 'same-structure'
  | 'same-theme'
  | 'same-sound';

export interface CharacterGroup {
  id: string;
  title: string;
  subtitle: string;
  rule: CharacterGroupRule;
  keywords: string[];
  characters: string[];
}

type StructureType = NonNullable<HanziItem['structureType']>;

interface HanziSeed {
  id: string;
  character: string;
  pinyin: string;
  meaning: string;
  radical: string;
  strokeCount: number;
  structureType: StructureType;
  structureHint: string;
  parts: string[];
  observationHint: string;
  vocabExamples: Array<{ word: string; gloss: string }>;
  poem?: {
    lineText: string;
    poemTitle: string;
    author: string;
    dynasty: string;
    meaningInLine: string;
    kidExplanation: string;
  };
}

function makeCharactersIncluded(lineText: string) {
  return [...new Set(lineText.replace(/[，。！？、；：]/g, '').split(''))];
}

function toHanziItem(seed: HanziSeed): HanziItem {
  return {
    id: seed.id,
    character: seed.character,
    pinyin: seed.pinyin,
    meaning: seed.meaning,
    radical: seed.radical,
    strokeCount: seed.strokeCount,
    animationLabel: `先看清楚「${seed.character}」的筆順，再跟著寫。`,
    introText: `「${seed.character}」是一個常用繁體字，先看動畫，再自己練寫。`,
    prompt: `把「${seed.character}」穩穩地描出來。`,
    encouragement: `很好，你已經完成「${seed.character}」的練習。`,
    projectTheme: '筆畫輔助工具',
    observationHint: seed.observationHint,
    missionPrompt: `今天先把「${seed.character}」寫穩。`,
    vocabExamples: seed.vocabExamples.map((example, index) => ({
      id: `${seed.id}-word-${index + 1}`,
      word: example.word,
      gloss: example.gloss,
    })),
    structureType: seed.structureType,
    structureHint: seed.structureHint,
    parts: seed.parts,
  };
}

function toPoemEntry(seed: HanziSeed): PoemLibraryEntry | null {
  if (!seed.poem) {
    return null;
  }

  return {
    id: `poem-${seed.id}`,
    lineText: seed.poem.lineText,
    poemTitle: seed.poem.poemTitle,
    author: seed.poem.author,
    dynasty: seed.poem.dynasty,
    focusCharacter: seed.character,
    charactersIncluded: makeCharactersIncluded(seed.poem.lineText),
    difficultyLevel: 'starter',
    themeTags: ['繁體工具字庫'],
    isApproved: true,
    meaningInLine: seed.poem.meaningInLine,
    kidExplanation: seed.poem.kidExplanation,
    sourceNote: `${seed.poem.poemTitle} 真實古詩原句`,
  };
}

