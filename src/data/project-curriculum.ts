import type { Course, HanziItem, HanziPoemLink, PoemLibraryEntry } from './curriculum';

function makeCharactersIncluded(lineText: string) {
  return [...new Set(lineText.replace(/[，。！？、；：]/g, '').split(''))];
}

function poemEntry(input: Omit<PoemLibraryEntry, 'charactersIncluded' | 'isApproved' | 'sourceNote'>): PoemLibraryEntry {
  return {
    ...input,
    charactersIncluded: makeCharactersIncluded(input.lineText),
    isApproved: true,
    sourceNote: `${input.poemTitle} 真实古诗原句`,
  };
}

